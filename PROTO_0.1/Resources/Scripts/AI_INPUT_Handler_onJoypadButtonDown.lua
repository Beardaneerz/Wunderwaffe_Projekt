function AI_INPUT.onJoypadButtonDown ( nJoypad, nButton )
--------------------------------------------------------------------------------
	
    --log.message ( nButton )

--------------------------------------------------------------------------------
-- MOVE
--------------------------------------------------------------------------------
    
    if ( nButton == 7) -- UP
    then
        table.setAt ( this.tInput ( ),0,true )
    end
    
    if ( nButton == 10) -- RIGHT
    then
        table.setAt ( this.tInput ( ),1,true )
    end
    
    if ( nButton == 8) -- DOWN
    then
        table.setAt ( this.tInput ( ),2,true )
    end
    
    if ( nButton == 9) -- LEFT
    then
        table.setAt ( this.tInput ( ),3,true )
    end
 
 --------------------------------------------------------------------------------
-- ACTION
--------------------------------------------------------------------------------

    if ( nButton == 0 )
    then
        log.message ( "TIR" )
        this.bFire ( true )
    end
	
--------------------------------------------------------------------------------
end

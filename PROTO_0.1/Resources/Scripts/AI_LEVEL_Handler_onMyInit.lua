function AI_LEVEL.onMyInit (  )
--------------------------------------------------------------------------------
	
    local hUser = application.getCurrentUser ( )
    
	-- SET HUD FOR MAP CHOICE
	hud.newTemplateInstance ( hUser,"HUD_MAP","MAP" )
    hud.callAction ( hUser,"MAP.SCALE_1" )
    
    this.bMap ( true )
--------------------------------------------------------------------------------
end

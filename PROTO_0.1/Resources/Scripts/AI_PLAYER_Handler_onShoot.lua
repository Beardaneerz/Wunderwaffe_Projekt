function AI_PLAYER.onShoot ( nType  )
--------------------------------------------------------------------------------
	
    
    if(nType == 1)
    then
        if(this.bShoot() )
        then
            object.postEvent ( this.getObject ( ),this.nCadence(),"AI_PLAYER","onShoot",nType )
            this.Weapon_1 ( )
        end
    end
    
    if(nType == 2)
    then
        if(this.bShoot ( ))
        then 
            this.Weapon_2 ( )
        else
            
        end
    end
    
--------------------------------------------------------------------------------
end

function AI_ENEMY.Destroyer ( )
--------------------------------------------------------------------------------
	
    local hUser = application.getCurrentUser ( )

	if(this.nPV ( ) <0 )
    then
        object.setVisible ( this.getObject ( ),false )
        sensor.setActiveAt(this.getObject ( ),0,false )
        this.nPatternType ( 0 ) 
        
        user.sendEvent ( hUser,"AI_SCORE","onAddScore",this.nScore ( ) )
       
    end
--------------------------------------------------------------------------------
end

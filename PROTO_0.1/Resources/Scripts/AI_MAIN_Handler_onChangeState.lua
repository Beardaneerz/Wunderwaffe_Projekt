function AI_MAIN.onChangeState ( nState )
--------------------------------------------------------------------------------
	
	if ( nState == 0 )
    then
    
        this.IDLE ( )
    
    elseif ( nState == 1 )
    then
    
        this.PLAY ( )
    
    end
	
--------------------------------------------------------------------------------
end
